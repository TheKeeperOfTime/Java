package _09_Loops;

public class _04_for_each {

	public static void main(String[] args) {
		
		
		
		String[] comedians = {"George Carlin", "Louis CK", "Paul O'Connnor"};
		
		for(String comedian : comedians){
			System.out.println(comedian);
		}
		
		
		String[] theBeatles = {"Paul", "George", "John", "Ringo!"};
		
		for(String beatles : theBeatles){
			System.out.println(beatles);
		}			
			String s = "Hello";
			for(char ch : s.toCharArray()){
				System.out.println(ch);
			}
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
	}

}
