package _09_Loops;

public class _04_for_reach {

	public static void main(String[] args) {
		
		
		
		String[] comedians = {"George Carlin", "Louis CK", "Paul O'Connnor"};
		
		for(String comedian : comedians){
			System.out.println(comedian);
		}
		
		
		String[] bands = {"The New Mastersounds", "A Tribe Called Quest", "Frank Turner"};
			
			for(String band : bands){
				System.out.println(band);
			}
			
			String s = "Hello";
			for(char ch : s.toCharArray()){
				System.out.println(ch);
			}
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
	}

}
